# Operator Precedence
# parenthesis overrides the order of ops
# exponentiation 2 ** 3
# multiplication or division
# addition or subtraction
x = 10 + 3 * 2
print(x)
x = 10 + 3 * 2 ** 2
print(x)
x = (10 + 3) * 2 ** 2
print(x)
x = (2 + 3) * 10 - 3
print(x)

